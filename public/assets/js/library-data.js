function librarySentenceFor(note, category, type) {
  const lower = note.toLowerCase();
  if (type === "Atmospheric Accord") {
    return `${note} is a place-like accord built to suggest atmosphere, memory, and texture rather than a single literal ingredient. Use it when the perfume needs a recognizable scene inside Indische World.`;
  }
  if (lower.includes("tea")) {
    return `${note} brings a quiet aromatic tea impression with gentle bitterness, steam, and refined freshness. It works well when a composition needs calm clarity instead of loud sweetness.`;
  }
  if (lower.includes("powder") || ["orris", "iris", "heliotrope"].some((word) => lower.includes(word))) {
    return `${note} gives a soft powdery impression with cosmetic elegance and a muted vintage texture. It can blur sharp edges and make floral, musk, or gourmand ideas feel more intimate.`;
  }
  if (lower.includes("musk")) {
    return `${note} adds a clean, skin-like softness that supports diffusion and wearability without taking over the composition.`;
  }
  if (lower.includes("wood") || ["cedar", "sandal", "oud", "hinoki"].some((word) => lower.includes(word))) {
    return `${note} provides woody structure and drydown stability, helping brighter materials settle into a polished base.`;
  }
  if (lower.includes("rose") || lower.includes("jasmine") || lower.includes("blossom") || lower.includes("flower")) {
    return `${note} gives a floral heart with petal texture and emotional presence, useful for adding elegance and body to the perfume.`;
  }
  if (category === "Citrus") {
    return `${note} brings sparkling citrus brightness with peel-like freshness, ideal for opening a formula with lift and clarity.`;
  }
  if (category === "Fruity") {
    return `${note} gives a juicy fruit impression that can make a fragrance feel vivid, playful, and approachable when balanced with woods, musks, or tea.`;
  }
  if (category === "Green") {
    return `${note} adds leafy botanical freshness with a natural green texture, useful for garden, glasshouse, and rain-washed concepts.`;
  }
  if (category === "Spicy") {
    return `${note} adds warmth, movement, and textured contrast, especially useful when a composition needs energy or a handcrafted edge.`;
  }
  if (category === "Gourmand") {
    return `${note} brings edible warmth and comfort, best used with restraint so the perfume remains polished rather than overly sweet.`;
  }
  if (category === "Earthy") {
    return `${note} gives grounded natural depth with soil, moss, root, or mineral character for more atmospheric drydowns.`;
  }
  if (category === "Amber & Resin") {
    return `${note} adds golden warmth, resinous depth, and lasting richness for a more ceremonial or evening-like trail.`;
  }
  if (category === "Marine & Air") {
    return `${note} creates airy, watery space and helps a perfume feel open, transparent, and fresh.`;
  }
  if (category === "Leather & Tobacco") {
    return `${note} introduces darker texture, warmth, and vintage character for more tailored or nocturnal fragrance stories.`;
  }
  return `${note} is a ${category.toLowerCase()} material used to shape the perfume's mood, structure, and storytelling direction.`;
}

function libraryDetailsFor(note, category, type) {
  const preset = libraryFamilyDetails[category] || {
    mood: ["Balanced", "Textural"],
    bestUsedFor: ["Supporting structure", "Creative accords"],
    pairsWellWith: ["Musk", "Woods", "Citrus"],
    avoidIf: ["You want a very minimal composition"]
  };
  return {
    mood: preset.mood,
    bestUsedFor: preset.bestUsedFor,
    pairsWellWith: preset.pairsWellWith,
    avoidIf: preset.avoidIf,
    description: librarySentenceFor(note, category, type)
  };
}

