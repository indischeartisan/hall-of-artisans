function librarySlugify(text) {
  return String(text || "")
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

function libraryTypeFor(note, category) {
  if (category === "Atmospheric") return "Atmospheric Accord";
  if (note.toLowerCase().includes("accord")) return "Accord";
  return "Note";
}

function libraryRoleFor(category) {
  if (["Citrus", "Green", "Tea & Aromatic", "Spicy", "Marine & Air"].includes(category)) return ["Top"];
  if (["Fruity", "Floral", "Powdery", "Atmospheric"].includes(category)) return ["Heart"];
  if (["Gourmand", "Woods", "Earthy", "Amber & Resin", "Musk", "Leather & Tobacco"].includes(category)) return ["Base"];
  return [];
}

